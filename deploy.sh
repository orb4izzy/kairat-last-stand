# –ù–∞—Å—Ç—Ä–æ–π–∫–∞ Nginx
setup_nginx() {
    print_header "–ù–∞—Å—Ç—Ä–æ–π–∫–∞ Nginx"
    
    # –°–æ–∑–¥–∞–Ω–∏–µ –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–∏ —Å–∞–π—Ç–∞
    sudo tee /etc/nginx/sites-available/kairat-game > /dev/null << EOF
server {
    listen 80;
    listen [::]:80;
    server_name $APP_DOMAIN www.$APP_DOMAIN;
    root /var/www/kairat-game/public;

    add_header X-Frame-Options "SAMEORIGIN";
    add_header X-Content-Type-Options "nosniff";

    index index.php;

    charset utf-8;

    location / {
        try_files \$uri \$uri/ /index.php?\$query_string;
    }

    location = /favicon.ico { access_log off; log_not_found off; }
    location = /robots.txt  { access_log off; log_not_found off; }

    error_page 404 /index.php;

    location ~ \.php$ {
        fastcgi_pass unix:/var/run/php/php8.1-fpm.sock;
        fastcgi_param SCRIPT_FILENAME \$realpath_root\$fastcgi_script_name;
        include fastcgi_params;
    }

    location ~ /\.(?!well-known).* {
        deny all;
    }
}
EOF
    
    # –ê–∫—Ç–∏–≤–∞—Ü–∏—è —Å–∞–π—Ç–∞
    sudo ln -sf /etc/nginx/sites-available/kairat-game /etc/nginx/sites-enabled/
    sudo rm -f /etc/nginx/sites-enabled/default
    
    # –ü—Ä–æ–≤–µ—Ä–∫–∞ –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–∏
    sudo nginx -t
    
    # –ü–µ—Ä–µ–∑–∞–ø—É—Å–∫ Nginx
    sudo systemctl restart nginx
    sudo systemctl enable nginx
    
    print_message "Nginx –Ω–∞—Å—Ç—Ä–æ–µ–Ω"
}

